#include "fractol.h"

int	check_arg(char *s)
{
	int	i;

	i = 0;
	if ((s[i] >= 48 && s[i] <= 57) || s[i] == '-' || s[i] == '+')
	{
		while (s[i])
		{
			if (ft_isdigit(s[i]) == 0)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
